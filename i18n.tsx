interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string | string[];
  dir: 'ltr' | 'rtl';
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

const getInitialLanguage = (): Language => {
    try {
        const storedLang = localStorage.getItem(LANGUAGE_STORAGE_KEY);
        if (storedLang && Object.keys(languageMap).includes(storedLang)) {
            return storedLang as Language;
        }
        const browserLang = navigator.language.split('-')[0];
        if (browserLang && Object.keys(languageMap).includes(browserLang)) {
           return browserLang as Language;
        }
    } catch (e) {
        console.error("Could not access localStorage, defaulting to English.", e);
    }
    return 'id'; // Default to Indonesian
};

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>(getInitialLanguage);

  useEffect(() => {
    try {
        localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    } catch (e) {
        console.error("Could not save language to localStorage.", e);
    }
  }, [language]);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
  };
  
  const t = useCallback((key: string): string | string[] => {
    const keys = key.split('.');

    const getTranslation = (lang: Language): string | string[] | Translations | undefined => {
        let res: any = translations[lang];
        for (const k of keys) {
            res = res?.[k];
        }
        return res;
    };

    const result = getTranslation(language) ?? getTranslation('en') ?? getTranslation('id');

    if (result === undefined) {
        return key;
    }

    if (typeof result === 'object' && result !== null && !Array.isArray(result)) {
        console.warn(`Translation for key '${key}' resolves to an object, which cannot be rendered. Returning the key itself.`);
        return key;
    }
    
    return result as string | string[];
  }, [language]);

  const dir = language === 'ar' ? 'rtl' : 'ltr';

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, dir }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLocalization = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLocalization must be used within a LanguageProvider');
  }
  return context;
};
interface LanguageContextType {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: string, replacements?: { [key: string]: string }) => string | string[] | Translations;
  dir: 'ltr' | 'rtl';
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>(() => {
    try {
      const storedLang = localStorage.getItem(LANGUAGE_STORAGE_KEY) as Language;
      return storedLang && languageMap[storedLang] ? storedLang : 'en';
    } catch (e) {
      return 'en';
    }
  });

  const dir = language === 'ar' ? 'rtl' : 'ltr';

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    try {
        localStorage.setItem(LANGUAGE_STORAGE_KEY, lang);
    } catch(e) {
        console.error("Failed to save language to localStorage", e);
    }
  };

  const t = useCallback((key: string, replacements: { [key: string]: string } = {}) => {
    const keys = key.split('.');
    let result: any = translations[language];
    
    for (const k of keys) {
        if (result && typeof result === 'object' && k in result) {
            result = result[k];
        } else {
            // Fallback to English
            result = translations['en'];
            for (const k_en of keys) {
                 if (result && typeof result === 'object' && k_en in result) {
                    result = result[k_en];
                } else {
                    return key; // Key not found in both languages
                }
            }
            break; // Found in English, break the loop
        }
    }

    if (typeof result === 'string') {
        Object.keys(replacements).forEach(placeholder => {
            result = result.replace(`{${placeholder}}`, replacements[placeholder]);
        });
    }

    return result;
  }, [language]);


  useEffect(() => {
    document.documentElement.lang = language;
    document.documentElement.dir = dir;
  }, [language, dir]);

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, dir }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLocalization = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLocalization must be used within a LanguageProvider');
  }
  return context;
};
